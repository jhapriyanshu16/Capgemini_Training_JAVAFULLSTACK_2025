package coreJavaCapgemini;

public class Datatype {
	public static void main(String[] args) {
		byte num1;
		num1 = 2;
		System.out.println(num1);
		short num2;
		num2 = 20;
		System.out.println(num2);
		int num3;
		num3 = 200;
		System.out.println(num3);
		char c;
		c = 'f';
		System.out.println(c);
		
	}

}
